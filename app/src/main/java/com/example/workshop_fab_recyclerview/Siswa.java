package com.example.workshop_fab_recyclerview;

public class Siswa {
    String nama, instansi;

    public Siswa(String nama, String instansi) {
        this.nama = nama;
        this.instansi = instansi;
    }

    public String getNama() {
        return nama;
    }

    public String getInstansi() {
        return instansi;
    }
}
